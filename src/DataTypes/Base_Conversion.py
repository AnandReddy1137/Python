print('''
You can convert from any base to any base using bin(),oct(),hec()''')

print("bin(): convert from any base to binary")
print("bin(15): ",bin(15))
print("bin(0o101): ",bin(0o101))
print("bin(0o156): ",bin(0o156))
print("bin(0xBEEF): ",bin(0xBEEF))

print("oct(): convert from any base to Octal")
print("oct(15): ",oct(15))
print("oct(0o101): ",oct(0o101))
print("oct(0b1001): ",oct(0b1001))
print("oct(0xBEEF): ",oct(0xBEEF))

print("hex(): convert from any base to Hexadecimal")
print("hex(15): ",hex(15))
print("hex(0o101): ",hex(0o101))
print("hex(0b1001): ",hex(0b1001))
print("hex(0o123): ",hex(0o123))
